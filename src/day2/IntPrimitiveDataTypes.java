package day2;

public class IntPrimitiveDataTypes {


    public static void main(String[] args) {

        // minimum value of integer
        int minIntValue = Integer.MIN_VALUE;
        System.out.println("minIntValue = " + minIntValue);


        // maximum value of integer
        int maxIntValue = Integer.MAX_VALUE;
        System.out.println("maxIntValue = " + maxIntValue);

        // default value of integer
        // default value of int = 0;

        int number1 = 1500000000;
        int number2 = 1000000000;

        System.out.println(number1+number2);







    }
}

package day1;

public class MainMethod {

    public static void main(String[] args) {

        System.out.println("I live in New York");
        System.out.println("I don`t leave in New Jersey");

        System.out.println("========================");

        System.out.println("I live in New York \nI don`t leave in New Jersey");
        // \n Escape character is used when you want to bring part of your code to the next line

        System.out.println("========================");

        System.out.println("My name is Furkat Ali.      I am from US");
        System.out.println("My name is Furkat Ali. \t\tI am from US");

        // \t Escape character is used when you want to replace TAB key

        System.out.println("My Name is \"Furkat Ali\"");

        // \" Escape character is used when you want to convert " to a String

        System.out.println("I love \\US\\");

        // \\ Escape character is used when you want to convert \ to a String











    }

}



package day11;

public class ArithmeticOperators {
    public static void main(String[] args) {

        // plus (+)

        int a = 5;
        int b = 6;

        System.out.println("total = " + a + b); // 56
        System.out.println("total1 = " + (a+b)); // 11
        System.out.println("total2 = " + (a) + (b)); // 56

        // minus (-)
        int x = 10;
        int y = 6;
        System.out.println("minus total = " + (x - y) );

        // multiplies (*)
        int c = 5;
        int d = 10;
        System.out.println("multiply total = " + 5*10);
        System.out.println("multiply total = " + (5*10));
        System.out.println("multiply total = " + (5)*(10));

        // divides (/)
        int e = 10;
        int f = 2;
        System.out.println("division total = " +e/f);
        System.out.println("division total = " +(e/f));
        System.out.println("division total = " +(e)/(f));







    }
}

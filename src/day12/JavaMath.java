package day12;

public class JavaMath {
    public static void main(String[] args) {

    // Math.abs(int);
        System.out.println("Mathabs1 " + Math.abs(-500));
        System.out.println("Mathabs2 " + Math.abs(500));

    // Math.max(int1, int2);
        System.out.println("Math.max " + Math.max(10,50));

    // Math.min(int1,int2);
        System.out.println("Math.min " + Math.min(10,100));

    // Math.round(a);
        System.out.println("Math.round " + Math.round(2.5));
        System.out.println("Math.round " + Math.round(2.4));

    // Math.sqrt(a);
        System.out.println("Math.sqrt " +  Math.sqrt(25));

    // Math.pow(a,b);
        System.out.println("Math.power " + Math.pow(3,3));
        System.out.println("Math.power " + Math.pow(2,5));

    // Math.ceil(a);
        System.out.println("Math.ceil " + Math.ceil(2.001));

    // Math.floor(a);
        System.out.println("Math.floor " + Math.floor(2.9999999));



    }
}
